## Funcion sumar
# @param a Primer numero a sumar
# @param b Segundo numero a sumar
# @return int
def sumar(a: int, b: int) -> int:
    return a + b


## Funcion restar
# @param a Primer numero a restar
# @param b Segundo numero a restar
# @return int
def restar(a: int, b: int) -> int:
    return a - b
